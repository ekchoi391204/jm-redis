SET NAMES utf8mb4;

CREATE DATABASE IF NOT EXISTS jm_market
  DEFAULT CHARACTER SET utf8mb4
  DEFAULT COLLATE utf8mb4_unicode_ci;

CREATE USER IF NOT EXISTS 'jm_user'@'%' IDENTIFIED BY 'jm_password';
GRANT ALL PRIVILEGES ON jm_market.* TO 'jm_user'@'%';
FLUSH PRIVILEGES;

USE jm_market;

CREATE TABLE IF NOT EXISTS MEMBER (
  MEMBER_NO INT NOT NULL AUTO_INCREMENT,
  MEMBER_ID VARCHAR(100) NOT NULL,
  MEMBER_PASSWD VARCHAR(255) NOT NULL,
  MEMBER_NAME VARCHAR(100) NOT NULL,
  MEMBER_BIRTH DATE NULL,
  MEMBER_EMAIL VARCHAR(255) NULL,
  MEMBER_PHONE VARCHAR(50) NULL,
  MEMBER_ZIPCODE VARCHAR(20) NULL,
  MEMBER_ADDR1 VARCHAR(255) NULL,
  MEMBER_ADDR2 VARCHAR(255) NULL,
  MEMBER_DATE DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  MEMBER_GRADE VARCHAR(30) NOT NULL DEFAULT 'NORMAL',
  MEMBER_TOTAL INT NOT NULL DEFAULT 0,
  MEMBER_LOG DATETIME NULL DEFAULT CURRENT_TIMESTAMP,
  MEMBER_DELETE CHAR(1) NOT NULL DEFAULT '0',
  EMAIL_AGREE CHAR(1) NULL DEFAULT '0',
  SMS_AGREE CHAR(1) NULL DEFAULT '0',
  PRIMARY KEY (MEMBER_NO),
  UNIQUE KEY UK_MEMBER_ID (MEMBER_ID),
  KEY IX_MEMBER_EMAIL (MEMBER_EMAIL)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS POINT (
  POINT_NO INT NOT NULL AUTO_INCREMENT,
  MEMBER_NO INT NOT NULL,
  POINT_VAL INT NOT NULL DEFAULT 0,
  POINT_SAVE_DATE DATETIME NULL,
